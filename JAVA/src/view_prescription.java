    public void CreatePrescriptionTable()
    {
        try {
            pst=conn.prepareStatement("select *from prescription" );
            
             rs=pst.executeQuery();
                ResultSetMetaData rsm=(ResultSetMetaData) rs.getMetaData();
                int c;
                c=rsm.getColumnCount();
                DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
                df.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2=new Vector();
                    
                    for(int i=1; i<c; i++)
                    {
                        v2.add(rs.getString("patient_id"));
                        v2.add(rs.getString("doctorname"));
                        v2.add(rs.getString("deseasetype"));
                        v2.add(rs.getString("description"));
                    }
                    df.addRow(v2);
                }
        
        } catch (SQLException ex) {
            Logger.getLogger(view_prescription.class.getName()).log(Level.SEVERE, null, ex);
        }
               
    }
